const weekdays = ['monday','tuesday','wednesday']
console.log(weekdays);

weekdays.push('Thursday')
console.log(weekdays);

weekdays.unshift('Sunday')
console.log(weekdays);

weekdays.push(2)
console.log(weekdays);

weekdays.pop()
console.log(weekdays);

weekdays.shift()
console.log(weekdays);